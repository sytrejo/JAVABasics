package java_basics;

public class Variables {

	public static void main(String[] args) {
//		Declare the variable by defining the data type
		int x;
		
//		assign a value to the variable x
		x = 34;
		
//		print out the value of x
		System.out.println(x);
		
//		re-assign value or assign new value to x
		x = 23;
		
//		print out the new value of x
		System.out.println(x);
		
//		Declare a string
		String words;
		
//		characters can go into words
		words = "this is a sentence";
		
//		print out the string assigned to words
		System.out.println(words);
		
// You can combine the declaration and value assignment of a variable into one line of code
		int y = 40;
		String words2 ="Happy day!";
		
//		print out new variables
		System.out.println( y);
		System.out.println(words2);

	}

}
